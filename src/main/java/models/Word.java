package models;

public class Word {
	
	private String word;
	
	private int ocurrence;
	
	public void setWord(String w) {
		this.word = w;
	}

	public String getWord() {
		return word;
	}	
	
	public void setOcurrence(int n) {
		this.ocurrence = n;
	}

	public int getOcurrence() {
		return ocurrence;
	}	

}
